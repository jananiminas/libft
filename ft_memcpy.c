#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char		*d;
	const unsigned char	*s;

	if (!dst && !src)
		return (0);
	s = src;
	d = dst;
	while (n-- > 0)
		*d++ = *s++;
	return (dst);
}
